// Função Built in: Math.
// Função usada para trabalhar com números.


let maiorNumero = Math.max(1,4,2,3); // Método max, encontra o maior valor.

console.log(maiorNumero);

let menorNumero = Math.min(10,20,30,40); // Método min, encontra o menor valor.

console.log(menorNumero);

let arrendondarNumero = Math.round(5.2222222); // Método round , arredonda para baixo.

console.log(arrendondarNumero);

let arrendondarParaCima = Math.ceil(5.22222); // Método ceil, arredonda para cima.

console.log(arrendondarParaCima);