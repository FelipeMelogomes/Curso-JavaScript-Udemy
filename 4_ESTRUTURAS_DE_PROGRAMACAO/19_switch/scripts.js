let nome = "Felipe";

switch(nome) {
  case "Felipe":
    console.log("O nome é Felipe");
    break;
  case "Maria":
    console.log("O nome é Maria");
    break;
  default:
    console.log("O nome não foi encontrado");
    break;
}


if(nome == "Felipe") {
  console.log("O nome é Felipe");
} else {
  console.log("O nome não foi encontrado");
}
